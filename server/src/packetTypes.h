#pragma once 

#include <cstdint>

enum class PacketType : uint32_t {
	// only receive
	AUTHORIZE,
	LOGOUT,
	LOGOUT_AND_STOP,
	GET_FRIEND_INFO,

	// receive and redirect
	START_CALLING,
	END_CALL,
	CALLING_END,
	CALL_ACCEPTED,
	CALL_DECLINED,

	// confirmations only send
	AUTHORIZE_SUCCESS,
	AUTHORIZE_FAIL,
	LOGOUT_OK,
	LOGOUT_AND_STOP_OK,
	GET_FRIEND_INFO_SUCCESS,
	GET_FRIEND_INFO_FAIL,
	START_CALLING_SUCCESS,
	START_CALLING_FAIL,
	END_CALL_OK,
	CALLING_END_OK,
	CALL_ACCEPTED_OK,
	CALL_DECLINED_OK,

	// special packets
	VOICE,
	PING,
	PING_SUCCESS
};

