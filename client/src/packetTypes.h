#pragma once 

#include <cstdint>

namespace calls {

enum class PacketType : uint32_t {
	// only send
	AUTHORIZE,
	LOGOUT,
	SHUTDOWN,
	DECLINE_ALL,
	GET_FRIEND_INFO,

	// send and receive
	START_CALLING,
	END_CALL,
	CALLING_END,
	CALL_ACCEPTED,
	CALL_DECLINED,

	// confirmations only receive
	AUTHORIZE_SUCCESS,
	AUTHORIZE_FAIL,
	LOGOUT_OK,
	SHUTDOWN_OK,
	GET_FRIEND_INFO_SUCCESS,
	GET_FRIEND_INFO_FAIL,
	START_CALLING_SUCCESS,
	START_CALLING_FAIL,
	END_CALL_OK,
	CALLING_END_OK,
	CALL_ACCEPTED_OK,
	CALL_DECLINED_OK,
	DECLINE_ALL_OK,

	// special packets
	VOICE,
	PING,
	PING_SUCCESS
};

}
